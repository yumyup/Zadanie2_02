 class ShapeCalculator {

    double squareBox(double a) {
        double sBox = 2 * a;
        return sBox;
    }

    double areaOfaCircle(double r) {
        double sCirc = (3.14 * r * r);
        return sCirc;
    }

    double circutTriangle(double a, double b, double c) {
        double cTria = (a + b + c);
        return cTria;

    }

    double circutRectangle(double a, double b) {
        double cRect = a + b;
        return cRect;
    }


}
